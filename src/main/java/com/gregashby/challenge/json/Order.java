package com.gregashby.challenge.json;

public class Order {
	
	private String editionCode = null;
	private String addonOfferingCode = null;
	private String pricingDuraction = null;
	public String getEditionCode() {
		return editionCode;
	}
	public void setEditionCode(String editionCode) {
		this.editionCode = editionCode;
	}
	public String getAddonOfferingCode() {
		return addonOfferingCode;
	}
	public void setAddonOfferingCode(String addonOfferingCode) {
		this.addonOfferingCode = addonOfferingCode;
	}
	public String getPricingDuraction() {
		return pricingDuraction;
	}
	public void setPricingDuraction(String pricingDuraction) {
		this.pricingDuraction = pricingDuraction;
	}
	

}
